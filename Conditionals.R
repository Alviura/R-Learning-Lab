# Conditional Statements -> They are used to execute different blocks of code based on certain conditions.
# The basic conditional statements are 
# 1) if
# 2) if...else
# 3) if...elseif...else

x <- 7
if(x > 10){
  print("x is greater than 10")
}else if(x > 5){
  print("x is greater than 5")
}else{
  print("x is less than or equal to 5")
}

# the above code summaries all the three condtions